    /**
     * Determine whether the user can view any models.
     *
     * @param  \App\Models\User  $user
     * @return mixed
     */
    public function viewAny(AdminUser  $user, $module)
    {
        return  Response::allow();
        $module = Module::where("slug", $module)->with('roles')->first();
        if (!empty($module)) {
            $roleData = $module->roles->map(function ($items) {
                return $items->id;
            })->toArray();
            if (in_array($user->role_id, $roleData)) {
                return  Response::allow();
            } else {
                return Response::deny('You don\'t have permission to access.');
            }
        } else {
            return Response::deny('You don\'t have permission to access.');
        }
    }

